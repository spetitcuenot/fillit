#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int i;
	int j;

	i = ft_strlen(src);
	j = 0;
	while (j != i + 1)
	{
		dst[j] = src[j];
		j++;
	}
	return (dst);
}
