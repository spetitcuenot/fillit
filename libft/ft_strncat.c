#include "libft.h"
#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;

	i = ft_strlen(s1);
	while (n-- && *s2)
		*(s1 + i++) = *(s2++);
	*(s1 + i) = '\0';
	return (s1);
}
